'''
Ví dụ đơn giản: Các phép toán cơ bản trong Python
Script này minh họa cách sử dụng các toán tử số học với hai biến.
'''

# Biến a lưu số nguyên 16
a = 16
# Biến b lưu số nguyên 10
b = 10

# Phép cộng: a + b = 26
print(a + b)
# Phép trừ: a - b = 6
print(a -  b)
# Phép nhân: a * b = 160
print(a * b)
# Phép chia: a / b = 1.6 (trả về số thực)
print(a  /  b)
# Phép chia lấy phần nguyên: a // b = 1 (chỉ lấy phần nguyên)
print(a  //  b)
# Phép chia lấy dư: a % b = 6 (trả về phần dư của phép chia)
print(a  %  b)
